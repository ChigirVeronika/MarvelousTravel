package by.bsuir.travel.dto;

public class PlacesTags {

    private String[] places;
    private String[] tags;

    public PlacesTags() {
    }

    public String[] getPlaces() {
        return places;
    }

    public void setPlaces(String[] places) {
        this.places = places;
    }

    public String[] getTags() {
        return tags;
    }

    public void setTags(String[] tags) {
        this.tags = tags;
    }
}
